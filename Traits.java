public class Traits
{
	private double health;
	private double stamina;
	private double speed;
	private double eyesight;
	private double intimacy;

	public void setHealth(double x)
	{
		this.health = x;
	}

	public double getHealth(double x)
	{
		return this.health;
	}

	public void setStamina(double x)
	{
		this.stamina = x;
	}

	public double getStamina()
	{
		return this.stamina;
	}

	public void setSpeed(double x)
	{
		this.speed = x;
	}

	public double getSpeed()
	{
		return this.speed;
	}

	public void setEyesight(double x)
	{
		this.eyesight = x;
	}

	public double getEyesight()
	{
		return this.eyesight;
	}
}